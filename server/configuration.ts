export const getConfigurationByIndex = (start: number, size: number) => {
  const data = configuration
    .map((item, index) => ({
      ...item,
      carouselId: 'cus' + index,
      title: `${item.title}-${index}`,
      model: index % 2 ? 'movie' : 'show',
    }))
    .slice(start, start + size);

  return {
    data,
    total: configuration.length,
    start: start,
    end: start + size,
  };
};
